					Lucero Natarén / Aquínoticias

					Fraude, extorsión, robo o asalto en la calle o en el transporte público, entre otros, son los 
					delitos más frecuentes en el país, de acuerdo con los resultados de la Encuesta Nacional de 
					Victimización y Percepción sobre Seguridad Pública (EVIPE) 2021.

					En Chiapas el delito más frecuente en 2020 fue el robo o asalto en la calle o en el transporte 
					público, aunque este último no se vive a tal magnitud como en el Estado de México, no significa 
					que no sucede en la entidad.

					Por otro lado, las principales preocupaciones que aquejan a la sociedad mexicana en este 2021 son 
					la inseguridad y el desempleo. A nivel nacional el 58.9 por ciento de las personas de 18 años de 
					edad y más considera la inseguridad como el problema más importante que a queja su diario vivir, 
					seguido del desempleo, con 41.5 por ciento y la salud con 40.2 por ciento.

					En el 2020, la inseguridad ocupaba un porcentaje del 68.2 por ciento, es decir, disminuyó ante la 
					pandemia, sin embargo, el desempleo incrementó un 5.1 por ciento, pues pasó de 36.4 a 41.5 por 
					ciento. También la preocupación por la salud incrementó un 4 por ciento, pues pasó de 36.2 a un 
					40.2 por ciento.

					La ENVIPE estimó que 75.6 por ciento de la población mayor de 18 años considera que vivir en su 
					estado es inseguro a consecuencia de la delincuencia durante el período de marzo y abril de 2021, 
					cifra estadísticamente menor a la  estimada en la edición anterior de la encuesta.

					La ENVIPE también estima a nivel nacional que 38.6 por ciento de la población de 18 años de edad y 
					más se sintió segura al caminar por la noche en los alrededores de su vivienda, en las mujeres se 
					estimó 31.1 por ciento, mientras que en hombres 47 por ciento.

					Sin embargo, en Chiapas, sólo cinco de cada 10 personas encuestadas contestó que se siente segura 
					de caminar sola por las noches en los alrededores de su vivienda.

					La ENVIPE estimó que, durante 2020, se generaron 27.6 millones de delitos asociados a 21.2 millones 
					de víctimas. Esto representa una tasa de 1.3 delitos por víctima (en 2019 fue de 1.4).


